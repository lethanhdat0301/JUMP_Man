    static int frameY =0;
    bool isJumping = true;
int main( int argc, char* args[] )
{
	//Start up SDL and create window
	if( !init() )
	{
		printf( "Failed to initialize!\n" );
	}
	else
	{
		//Load media
		if( !loadMedia() )
		{
			printf( "Failed to load media!\n" );
		}
		else
		{
			//Main loop flag
			bool quit = false;

			//Event handler
			SDL_Event e;

			//The dot that will be moving around on the screen
			Dot dot;

			Object object;

			//The background scrolling offset
			int scrollingOffset = 0;



			//While application is running
			while( !quit )
			{
				//Handle events on queue
				while( SDL_PollEvent( &e ) != 0 )
				{
					//User requests quit
					if( e.type == SDL_QUIT )
					{
						quit = true;
					} else if(e.type == SDL_KEYDOWN){
                    switch(e.key.keysym.sym)
                        {
                    case SDLK_UP:
                       // Todo
                        isJumping = true;
                       break;

                        }
                        }

					//Handle input for the dot
					dot.handleEvent( e );
                    }
                    if(isJumping){
                    dot.SetFrame(frameX,0, 5);
                    ++frameX;
                    }
                    if (frameX / 5 >= 6) {
                        frameX = 0;
                        isJumping = false;
                    }
                    //Move the dot
                    dot.move();

				//Scroll background
				--scrollingOffset;
				if( scrollingOffset < -gBGTexture.getWidth() )
				{
					scrollingOffset = 0;
				}

                if( scrollingOffset < -gObTexture.getWidth() )
				{
					scrollingOffset = 0;
				}

				//Clear screen
				SDL_SetRenderDrawColor( gRenderer, 0xFF, 0xFF, 0xFF, 0xFF );
				SDL_RenderClear( gRenderer );

				//Render background
				gBGTexture.render( scrollingOffset, 0 ,NULL);
				gBGTexture.render( scrollingOffset + gBGTexture.getWidth(), 0, NULL );

                gObTexture.render( scrollingOffset, 0 ,NULL);
				gObTexture.render( scrollingOffset + gObTexture.getWidth(), 0, NULL );
				//Render objects
				dot.render();
				object.render();

				//Update screen
				SDL_RenderPresent( gRenderer );
			}
		}
	}

	//Free resources and close SDL
	close();

	return 0;
}
